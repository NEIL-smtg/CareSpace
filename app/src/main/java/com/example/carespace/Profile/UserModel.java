package com.example.carespace.Profile;

public class UserModel {
    private String uid,name,email,contactNum,password,address, imgUrl;

    public UserModel() {
    }

    public UserModel(String uid, String name, String email, String contactNum, String password, String address, String imgUrl) {
        this.uid = uid;
        this.name = name;
        this.email = email;
        this.contactNum = contactNum;
        this.password = password;
        this.address = address;
        this.imgUrl = imgUrl;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getContactNum() {
        return contactNum;
    }

    public void setContactNum(String contactNum) {
        this.contactNum = contactNum;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }
}
